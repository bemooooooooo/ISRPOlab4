
def area(a):
    '''
    Calculates the area of a square
    :param a: square side size
    :type a: int or float
    :rtype: int or float
    :return: a*a - area of the square
    :example: area(4) = 16
    '''
    return a * a


def perimeter(a):
    '''
    Calculates the perimeter of a square
    :param a: square side size
    :type a: int or float
    :rtype: int or float
    :return: 4*a - perimeter of the square
    :example: perimeter(5) = 20
    '''
    return 4 * a